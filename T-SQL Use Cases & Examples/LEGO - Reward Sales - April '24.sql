--------------------------------------------PRODUCT TABLE----------------------------------------
-------------------------------------------------------------------------------------------------

IF OBJECT_ID ('tempdb..#LEGO') IS NOT NULL
	DROP TABLE #LEGO

SELECT *
	INTO #LEGO
	FROM DIM_PRODUCT P (NOLOCK)
	WHERE P.MANUFACTURE_CODE = 500687
		AND P.PROD_TYPE_CODE = '425'
		AND P.SUBCLASS_CODE = 'TBB'
		AND P.MERCHANDISE_FLG = 'YES'
GO

